package com.example.demo.models;

public class Users {
    private Long id;
    private String name;

    public Users(){}

    public Long getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public void setName(String name) {
        this.name = name;
    }

}



